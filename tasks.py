class BillingSystem:

    def __init__(self):
        self.products = {}
        self.cart = {}
        self.total = 0

    def add_product(self):
        name = input("Enter product name: ")
        price = float(input("Enter product price: "))
        self.products[name] = price
        print("Product Added!")

    def scan_product(self):
        name = input("Enter product to scan: ")
        qty = int(input("Enter quantity: "))

        if name in self.products:
            self.cart[name] = self.cart.get(name, 0) + qty
            self.total += self.products[name] * qty
            print("Product Scanned!")
        else:
            print("Product not found!")

    def apply_discount(self):
        discount = float(input("Enter discount %: "))
        self.total -= (self.total * discount / 100)
        print("Discount Applied!")

    def generate_bill(self):
        print("\n------ BILL ------")
        for name, qty in self.cart.items():
            price = self.products[name]
            print(name, "x", qty, "=", price * qty)
        print("Total Amount:", self.total)
        print("------------------")

    def record_transaction(self):
        print("Transaction Recorded Successfully!")

