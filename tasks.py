"""1.Password Manager: Securely store, add, edit, and retrieve user passwords using encryption."""

import hashlib
import json

class PasswordManager:

    def __init__(self):
        self.file = "passwords.json"

    def encrypt(self, password):
        return hashlib.sha256(password.encode()).hexdigest()

    def add_password(self, site, password):
        data = self.load()
        data[site] = self.encrypt(password)
        self.save(data)
        print("Password Added")

    def view_password(self, site):
        data = self.load()
        if site in data:
            print("Encrypted Password:", data[site])
        else:
            print("Site not found")

    def edit_password(self, site, new_password):
        data = self.load()
        if site in data:
            data[site] = self.encrypt(new_password)
            self.save(data)
            print("Password Updated")
        else:
            print("Site not found")

    def load(self):
        try:
            with open(self.file,"r") as f:
                return json.load(f)
        except:
            return {}

    def save(self,data):
        with open(self.file,"w") as f:
            json.dump(data,f)


