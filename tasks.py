"""3.Warehouse Automation System: Track goods movement, generate inventory reports, and forecast demand."""

class Inventory:
    def __init__(self):
        self.stock = {}

    def add_product(self):
        name = input("Enter product name: ")
        qty = int(input("Enter quantity: "))
        price = float(input("Enter price: "))

        if name in self.stock:
            self.stock[name][0] += qty
        else:
            self.stock[name] = [qty, price]

        print("Product Added Successfully!")

    def remove_product(self):
        name = input("Enter product name to remove: ")
        qty = int(input("Enter quantity to remove: "))

        if name in self.stock and self.stock[name][0] >= qty:
            self.stock[name][0] -= qty
            print("Product Removed Successfully!")
        else:
            print("Product Not Available or Not Enough Stock!")

    def show_report(self):
        print("\nInventory Report")
        total = 0

        for name in self.stock:
            qty, price = self.stock[name]
            value = qty * price
            total += value
            print(name, "Qty:", qty, "Value:", value)

        print("Total Stock Value:", total)


